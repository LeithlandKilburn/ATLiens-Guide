@Data
@DynamoDBTable(tableName = "words")
public class Word {

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String wordId;

    @DynamoDBIndexHashKey (globalSecondaryIndexName = "name-useRating-index")
    @DynamoDBAttribute
    private String name;

    @DynamoDBAttribute
    private String definition;

    @DynamoDBAttribute
    private String example;

    @DynamoDBAttribute
    private String videoUrl;

    @DynamoDBIndexRangeKey(globalSecondaryIndexName = "name-useRating-index")
    @DynamoDBAttribute
    private int useRating;

    // TODO: lookup if we can add categories list instead of 1,2,3
    @DynamoDBAttribute
    private String category1Id;

    @DynamoDBAttribute
    private String category2Id;

    @DynamoDBAttribute
    private String category3Id;

    // TODO: add explicit tag here

}
